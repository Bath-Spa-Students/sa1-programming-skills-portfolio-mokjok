# Start Counting one number at a time from 0 to 50
print("Counting up from 0 to 50:")
for i in range(0, 51, 1):
    print(i)

# one number at a time, counting down from 50 to 0.
print("\nCounting down from 50 to 0:")
for i in range(50, -1, -1):
    print(i)

# Counting one number at a time from 30 to 50
print("\nCounting up from 30 to 50:")
for i in range(30, 51, 1):
    print(i)

#Counting from 50 to 10 without recognizing any other numbers.
print("\nCounting down from 50 to 10, ignoring every other number:")
for i in range(50, 9, -2):
    print(i)

# Add 5 each time as you count up from 100 to 200
print("\nIncreasing by 5 each time from 100 to 200:")
for i in range(100, 201, 5):
    print(i)
